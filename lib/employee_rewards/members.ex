defmodule EmployeeRewards.Members do
  @moduledoc """
  The Members context.
  """

  import Ecto.Query, warn: false
  alias EmployeeRewards.Repo

  alias EmployeeRewards.Members.Member
  alias EmployeeRewards.Identity

  def reset_members_points do
    Repo.update_all(Member, set: [points: 50])
  end

  @doc """
  Returns the list of members.

  ## Examples

      iex> list_members()
      [%Member{}, ...]

  """
  def list_members do
    Repo.all(Member)
  end

  @doc """
  Gets a single member.

  Raises `Ecto.NoResultsError` if the Member does not exist.

  ## Examples

      iex> get_member!(123)
      %Member{}

      iex> get_member!(456)
      ** (Ecto.NoResultsError)

  """
  def get_member!(id), do: Repo.get!(Member, id)

  @doc """
  Creates a member.

  ## Examples

      iex> create_member(%{field: value})
      {:ok, %Member{}}

      iex> create_member(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  # REVIEW: maybe try not using raw foreign keys for changeset
  # HACK: we return credentials as they are needed in other controller
  # and we are not preloading it in member
  def register_member(attrs \\ %{}) do
    with {:ok, credentials} <- Identity.register_credentials(attrs) do
      member =
        %Member{}
        |> Member.changeset(%{credentials_id: credentials.id})
        |> Repo.insert()

      {:ok, member, credentials}
    end
  end

  @doc """
  Updates a member.

  ## Examples

      iex> update_member(member, %{field: new_value})
      {:ok, %Member{}}

      iex> update_member(member, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_member(%Member{} = member, attrs) do
    member
    |> Member.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a member.

  ## Examples

      iex> delete_member(member)
      {:ok, %Member{}}

      iex> delete_member(member)
      {:error, %Ecto.Changeset{}}

  """
  def delete_member(%Member{} = member) do
    Repo.delete(member)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking member changes.

  ## Examples

      iex> change_member(member)
      %Ecto.Changeset{data: %Member{}}

  """
  def change_member(%Member{} = member, attrs \\ %{}) do
    Member.changeset(member, attrs)
  end
end
